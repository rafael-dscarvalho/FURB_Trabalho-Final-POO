    private void JSalvarCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JSalvarCSVActionPerformed
        // --- JFILECHOOSER PARA SALVAR CSV ---
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Escolha onde salvar o arquivo CSV");
            fileChooser.setSelectedFile(new File("dados.csv"));

            int userSelection = fileChooser.showSaveDialog(this);
            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File arquivoParaSalvar = fileChooser.getSelectedFile();

                // Salva no arquivo escolhido pelo usuário
                controle.salvarCSV(arquivoParaSalvar.getAbsolutePath());

                JOptionPane.showMessageDialog(this, "Despesa cadastrada e dados salvos com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Despesa cadastrada, mas arquivo CSV não foi salvo.");
            }
    }//GEN-LAST:event_JSalvarCSVActionPerformed

